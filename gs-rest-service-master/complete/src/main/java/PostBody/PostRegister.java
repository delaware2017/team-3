package PostBody;

public class PostRegister {
	private String name;
	private String id;
	private String password;
	private String program;
	private int famSize;
	
	
	public String getName(){
		return name;
	}
	public String getId(){
		return id;
	}
	public String getPassword(){
		return password;
	}
	public String getProgram(){
		return program;
	}
	public int getFamSize(){
		return famSize;
	}
}
